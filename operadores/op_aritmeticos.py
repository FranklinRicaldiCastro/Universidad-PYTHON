# suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

# multiplicación y división (* y /)
multiplicación = 12 * 5
división = 12 / 5  # Devuelve un dato float (Flotante)

# Potenciación (exponente) (**)
exponente = 12 ** 5

# división baja (//)
división_baja = 12 // 5  # Devuelve entero, redondeado hacia abajo

# Resto ó Modulo (%)
resto = 12 % 7

# type(dato) nos devuelve que tipo de dato es
tipo_de_dato = type(división)
print(tipo_de_dato)

print(suma)
print(resto)
print(multiplicación)
print(división)
print(exponente)
print(división_baja)
print(resto)
