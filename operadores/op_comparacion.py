# Un (=) igual sirve para asignar un valor a algo
# Dos (==) para comparar dos valores si es True o False

igual_que = 5 == 6
distinto_que = 5 != 6
mayor_que = 5 > 6
menor_que = 5 < 6
mayor_o_igual_que = 5 >= 6
menor_o_igual_que = 5 <= 6

print(igual_que)
print(distinto_que)
print(mayor_que)
print(menor_que)
print(mayor_o_igual_que)
print(menor_o_igual_que)

